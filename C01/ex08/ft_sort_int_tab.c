#include <unistd.h>
#include <stdio.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	a;

	j = 0;
	while (j < size)
	{
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				a = tab[i + 1];
				tab[i + 1] = tab[i];
				tab[i] = a;
			}
			i++;
		}
		j++;
	}
}
